# Criar um programa que leia um valor em Reais e converta em Dólar

# Variáveis
# valor_dolar, valor_reais: number

# Início
def main():
    valor_dolar = 5.30
    valor_reais = int(input("Digite o valor em Reais: "))
    valor_dolar = valor_reais / valor_dolar
    print("O valor em Dólar é: ", valor_dolar)


main()
# Fim
